export interface PlanIssuePromptOptions {
  request: string;
  additionalContext?: string;
}

export function buildPlanIssuePrompt(options: PlanIssuePromptOptions): string {
  return `# タスク: 対話的な実装計画の立案

以下の依頼をもとに、ユーザーと対話しながら実装計画を立案してください。

## 依頼内容
\`\`\`
${options.request}
\`\`\`
${options.additionalContext ? `\n## 追加コンテキスト\n${options.additionalContext}\n` : ""}

## 【重要】必ず守るルール

### ルール1: 質問は必ず AskUserQuestion ツールを使う
- ユーザーへの質問は**絶対にテキストで出力しない**
- 質問が必要な場合は**必ず AskUserQuestion ツール**を使用する
- テキストで「どうしますか？」「選んでください」等と書くのは禁止

### ルール2: 計画は必ず指定フォーマットで出力する
- 計画は**必ず \`# 計画: [タイトル]\` で始める**
- このフォーマットがないと計画として認識されない

## 進め方

### フェーズ1: 初期理解と調査
1. コードベースを調査する（Glob, Grep, Readツールを使用）
2. 不明点や選択肢がある場合は **AskUserQuestion ツール**で質問する

### フェーズ2: 計画立案と出力
調査が完了したら、**必ず以下のフォーマットで**計画を出力する：

\`\`\`markdown
# 計画: [タイトル]

## 概要
[この計画で実現すること]

## 背景
[なぜこの変更が必要か]

## 変更対象
- \`path/to/file1.ts\`: [変更内容]
- \`path/to/file2.ts\`: [変更内容]

## 実装ステップ
1. [ステップ1の詳細]
2. [ステップ2の詳細]
3. [ステップ3の詳細]

## 受け入れ条件
- [ ] [条件1]
- [ ] [条件2]
- [ ] [条件3]

## 確認方法
[テストコマンドや確認手順]

## 注意事項
- [注意点1]
- [注意点2]
\`\`\`

### フェーズ3: ユーザー承認
計画を出力したら、**AskUserQuestion ツール**で承認を求める：
- 質問: 「この計画でよろしいですか？」
- 選択肢: 「承認」「修正が必要」「やり直し」

## 計画の詳細度

この計画は**Haikuモデル（軽量モデル）**で実装されます。
そのため、計画は非常に具体的で明確である必要があります：

- **変更するファイル**: 具体的なファイルパスを記載
- **変更内容**: 何をどう変更するか具体的に記載
- **実装ステップ**: 順序立てて具体的に
- **受け入れ条件**: モデルが自己判断できる具体的な基準

曖昧な指示や抽象的な表現は避け、実装者が迷わないレベルまで詳細化してください。

## 最終チェックリスト
- [ ] 質問はすべて AskUserQuestion ツールで行ったか？
- [ ] 計画は \`# 計画: [タイトル]\` で始まっているか？
- [ ] 承認は AskUserQuestion ツールで求めたか？
`;
}
